
import java.util.*;

public class FootballMatch {
    public String homeTeam;
    public String awayTeam;
    public int homeScore;
    public int awayScore;

    public FootballMatch(String home, String away) {
        homeTeam = home;
        awayTeam = away;
        homeScore = 0;
        awayScore = 0;
    }
}

	